package NivelBasico.TipoDeDados;

public class DadosNaoPrimitivos {
    public static void main(String[] args) {
        String nome = "Naruto Uzumaki";
        // metodos de DadosNaoPrimitivos;
       String nomeUpperCase = nome.toUpperCase(); // Tudo em caps lock;
       String nomeLowerCase = nome.toLowerCase(); // Tudo em minusculo;
        System.out.println(nomeUpperCase);
        System.out.println(nomeLowerCase);
    }
}
